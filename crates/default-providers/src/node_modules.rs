//! Node-style `node_modules` [`ModuleLoader`] — bare-specifier resolution for
//! **ES module** packages on disk (DECISIONS.md D22).
//!
//! This is a deliberate, narrow carve-out of the "no npm" non-goal (SPEC §125):
//! it *resolves* bare specifiers against an existing `node_modules` tree and
//! loads the packages that ship ES modules. It does **not** install anything,
//! load CommonJS packages (those are rejected with a clear message), support
//! `node:` builtins, or implement the full Node resolution algorithm.
//!
//! What is implemented ("minimal"):
//! - relative / absolute-path / `file:` specifiers — strict file resolution
//!   (explicit extensions, like [`FsModuleLoader`](crate::FsModuleLoader));
//! - bare specifiers (`pkg`, `pkg/sub`, `@scope/pkg`, `@scope/pkg/sub`) — walk
//!   `node_modules` upward from the referrer, read `package.json`, and resolve
//!   the subpath via `exports` (string form, and the `import`/`default`
//!   conditions of the object form) or, with no `exports`, `module` → `main` →
//!   `index.js`; the resolved target is probed for `.js`/`.mjs`/`.cjs` and an
//!   `index.*`.
//!
//! Subpath **patterns** are supported: an `exports` key with a single `*`
//! (e.g. `"./fn/*": "./fns/*.mjs"`) matches `pkg/fn/x`, substituting the
//! captured portion into the target (longest-prefix wins; exact keys win over
//! patterns). Not implemented (rejected or ignored): the full
//! condition-precedence algorithm (`node`/`browser`/nested beyond
//! `import`/`default`), `"imports"` (`#internal`), self-reference, and CommonJS
//! loading.
//!
//! Resolved modules are **realpath-canonicalized** (Node default,
//! preserve-symlinks off), so pnpm's symlinked store resolves transitively, and
//! resolution is **root-jailed** to the detected project root by default — a
//! path whose real location escapes the root is rejected (DECISIONS D25). A
//! standard single-project pnpm install resolves and passes the jail because its
//! `.pnpm` store lives under the project root; workspaces/global-links will need
//! the relax flag (D24's deferred CLI part).

