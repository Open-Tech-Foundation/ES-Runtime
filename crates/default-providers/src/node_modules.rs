    /// Builds a temp tree and returns (loader based at root, root path).
    fn temp_tree(label: &str) -> (NodeModuleLoader, PathBuf) {
        let root = std::env::temp_dir().join(format!("esrt-nm-{label}-{}", std::process::id()));
        let _ = std::fs::remove_dir_all(&root);
        std::fs::create_dir_all(&root).unwrap();
        let loader = NodeModuleLoader::with_base_dir(&root).unwrap();
        (loader, root)
    }

    fn write(path: PathBuf, contents: &str) {
        std::fs::create_dir_all(path.parent().unwrap()).unwrap();
        std::fs::write(path, contents).unwrap();
    }

