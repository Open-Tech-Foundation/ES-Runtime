    /// pnpm puts a package's deps as symlinked peers inside
    /// `.pnpm/<pkg>@<ver>/node_modules/`; resolving them requires realpath-ing the
    /// package to its real location so the dependency walk finds the peer (D25).
    #[cfg(unix)]
    #[tokio::test]
    async fn resolves_pnpm_symlinked_transitive_dep() {
        use std::os::unix::fs::symlink;
        let (loader, root) = temp_tree("pnpm");
        let nm = root.join("node_modules");
        let foo_real = nm.join(".pnpm/foo@1/node_modules/foo");
        let bar_real = nm.join(".pnpm/bar@2/node_modules/bar");
        write(
            foo_real.join("package.json"),
            r#"{ "type":"module", "main":"index.js" }"#,
        );
        write(
            foo_real.join("index.js"),
            "import { bar } from 'bar'; export const foo = 'F' + bar;",
        );
        write(
            bar_real.join("package.json"),
            r#"{ "type":"module", "main":"index.js" }"#,
        );
        write(bar_real.join("index.js"), "export const bar = 'B';");
        // foo's peer dep `bar`, symlinked inside foo's .pnpm node_modules.
        symlink(
            "../../bar@2/node_modules/bar",
            nm.join(".pnpm/foo@1/node_modules/bar"),
        )
        .unwrap();
        // top-level `node_modules/foo` symlink into the store.
        symlink(".pnpm/foo@1/node_modules/foo", nm.join("foo")).unwrap();

        // foo resolves to its REAL store path (realpath), not the symlink.
        let foo_id = loader.resolve("foo", "").await.unwrap();
        assert!(
            foo_id.contains("/.pnpm/foo@1/node_modules/foo/index.js"),
            "{foo_id}"
        );
        // bar, imported by foo, is found via the .pnpm peer symlink because foo's
        // referrer is now its real location.
        let bar_id = loader.resolve("bar", &foo_id).await.unwrap();
        assert!(
            bar_id.contains("/.pnpm/bar@2/node_modules/bar/index.js"),
            "{bar_id}"
        );
        std::fs::remove_dir_all(&root).ok();
    }

